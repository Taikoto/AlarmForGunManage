#define MAGNETISM_ON  0
#define MAGNETISM_OFF 1



//初始化PB5为输出口.并使能时钟		    
//ALARM IO初始化
void MAGNETISM_Init(void)
{
  GPIO_InitTypeDef  GPIO_InitStructure;
 	
  RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC, ENABLE);	 //使能PC端口时钟
	
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_13;				 //MAGNETISM-->PC.13 端口配置
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; 		 //推挽输出
  //GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
  GPIO_Init(GPIOC, &GPIO_InitStructure);					 //根据设定参数初始化GPIOC.13
  //GPIO_SetBits(GPIOC,GPIO_Pin_13);						 //PC.13 输出高
}

 
